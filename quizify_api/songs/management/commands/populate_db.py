class Command(BaseCommand):
    help = "Populate db with hardcoded tracks"

    def handle(self, *args, **options):
        # Delete all old songs / Categories
        Category.objects.all().delete()
        Song.objects.all().delete()

        # print category

        for category in categories:
            new_cat = Category.objects.create(name=category['name'])
            song_list = []
            for uri in category['songs']:
                new_song = Song.objects.create(spotify_uri=uri)
                song_list.append(new_song)
            new_cat.songs.add(*song_list)
            new_cat.save()

        self.stdout.write("Done")
